def seconds_in_a_year():
    
    Days_in_year = 365
    Hours_in_day = 24
    Minutes_in_hours = 60
    Seconds_in_minute = 60

    # Calculation
    total_seconds = Days_in_year * Hours_in_day * Minutes_in_hours * Seconds_in_minute

    print(f"There are {total_seconds} seconds in a year!")
    
  
if __name__ == "__main__":
    seconds_in_a_year() 


